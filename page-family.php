                        <h2>【家族信託】</h2>
                        <h3>家族信託とは？</h3>
                        <p>
                            認知症・物忘れなどによる高齢の親の財産管理を子供世代がサポートする制度。<br>
                            超高齢社会においては、相続対策だけではなく財産管理も含めた対策として注目される、家族信託を活用できるかがポイントとなります。
                        </p>
                        <p>（課題の背景）</p>
                        <p>
                            今までの相続対策として、相続の問題を予防するために遺言書を作成される方はいましたが、医療の発達によって平均寿命が伸び続ける現状では、認知症のリスクによる財産の凍結リスクが問題となってきております。
                        </p>
                        <p>（具体的な事例）</p>
                        <p>
                            　例えば、大きなお金が必要になった場合に定期預金を解約しようとすると、窓口での本人の意思確認が必要になります。いくら奥さんや息子さんだとしても、本人の口座は本人のものなので、家族といえども勝手に解約手続きなどはできません。<br>
                            　そして、認知機能が衰えてしまった状態の本人が窓口で対応していると、銀行員さんは定期預金の解約には、応じてくれずに成年後見制度の利用を勧めることになります
                        </p>
                        <h3>※成年後見制度とは・・・</h3>
                        <p>
                            　裁判所による監督のもと、第三者による全ての財産を管理する制度です。通常は、弁護士や司法書士などの専門家が選任されるケースが多く、亡くなるまで毎年の報酬が発生することから制度利用がなかなか進んでおりません。
                        </p>
                        <p>
                            　成年後見制度は、本人の財産を守るための必要な制度ではあるのですが、第三者に財産管理をされるのは抵抗がある。もしも信頼できる家族が近くにいるのであれば、家族にその役割を担ってもらいたい。
                        </p>
                        <p>　そんな場合に、提案させていただく制度が家族信託の制度です。</p>
                        <h3>家族信託を活用することで</h3>
                        <p>
                            　・実家の不動産を、施設に移って空き家になるタイミングで売却をしてもらう<br>
                            　・自分が銀行の窓口に行かなくても、信頼できる家族に支払いや入金の管理をしてもらう<br>
                            　・人に貸している不動産やアパートの契約や大規模修繕なども自分の代わりに管理、契約してもらう
                        </p>
                        <p>　などなど、他にも家族の状況に応じた「将来の困った」を予防するための対策を予め講じることができます。</p>
                        <h3>家族信託などの対策をとらなかった場合・・・</h3>
                        <p>
                            ・認知症になってしまい、預貯金の解約ができなくなり、事実上、口座が塩漬けの状態となってしまう。<br>
                            　→親の介護費用について親の財産での支払いができなくなり、現役世代の子供が立替えて支払うことに・・・<br>
                            ・認知症になってしまい、自宅での生活も難しいので施設に移るが支払いの原資とする予定の不動産売却が、本人の能力では認められないため空き家状態に・・・
                        </p>
                        <h3>次の事例に当てはまる場合には、家族信託の活用を検討されることをお勧めいたします。</h3>
                        <p>
                            ・ 子供世代に介護のことなど金銭的にもなるべく迷惑をかけたくない<br>
                            ・ 自宅の他の金融資産はあまり多くない<br>
                            ・ 自分にもしものことがあった場合に夫/妻のことも心配だ<br>
                            ・ 個人名義の収益不動産があり、自分自身で管理をしている<br>
                            ・ 老朽化した、収益アパートを所有している<br>
                            ・ 少し物忘れが増えてきているので、財産管理が心配だ<br>
                            ・ 障害のあるこどもがおり、自分がなくなった後のことが心配だ<br>
                            ・ 先祖代々の土地について、他家に流出しないような相続対策を取りたい
                        </p>
                        <h3>このような場合には、家族信託をお勧めできません。</h3>
                        <p>
                            ・ 信頼できるご家族がいない場合<br>
                            ・ 認知機能が著しく低下しており、判断ができる状態でない場合<br>
                            ・ 節税対策として検討をしている場合
                        </p>
                        <p></p>
                        <p>
                            　家族信託制度は、最先端の優れた制度でありますが、長期的な契約のため間違えて設計してしまうと、かえってトラブルのもとにもなってしまうケースもございます。
                            しっかりと家族間のコミュニケーションをサポートし、末永く安心できる家族信託の設計をサポートしてまいります。
                        </p>
